import random
# imports entire random library

# random float between 0 and 1 eg 0.567233900626328
print(random.random())

# random float between 1 and 10 eg 1.4615378077421968
print(random.uniform(1,10))

# random integer between 1 and 10 
print(random.randint(1,10))

# random integer between 1 and 6 -> like a die for a game.
print(random.randint(1,6))